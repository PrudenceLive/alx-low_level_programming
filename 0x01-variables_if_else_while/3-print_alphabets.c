#include <stdio.h>
/**
 * main -Entrypoint
 *
 * Return: Always 0 (success)
 */

int main(void)
{
	char c;

	char d;

	c = 'a';
	d = 'A';
	/* while loop */
	while
		(c <= 'z');
	{
			putchar(c);
			c++;
		}
	while
		(d <= 'Z');
	{
		putchar('c');
		d++;
	/* end while loop */
		}
	putchar('\n');
	return (0);
}
